def detect_intent(text):
    """اكتشف نية المستخدم من النص"""
    t = text.lower().strip()

    if any(kw in t for kw in ABOUT_KEYWORDS):
        return "about"
    if any(kw in t for kw in TRIGGER_KEYWORDS):
        return "trigger"
    if any(kw in t for kw in NOT_NOW_KEYWORDS):
        return "not_now"
    if any(kw in t for kw in POSITIVE_KEYWORDS):
        return "positive"
    if t in ["1", "٢", "2", "٣", "3", "١"]:
        return "choice_" + t

    return "unknown"


def parse_type_answer(text):
    """تحليل إجابة نوع الشقة"""
    t = text.lower()
    if any(w in t for w in ["1", "١", "إيجار سنوي", "ايجار سنوي", "سنوي", "ايجار"]):
        return "إيجار سنوي"
    elif any(w in t for w in ["2", "٢", "شراء", "تمليك", "بيع"]):
        return "شراء (تمليك)"
    elif any(w in t for w in ["3", "٣", "مفروش", "مفروشة"]):
        return "إيجار مفروش"
    return text


def validate_phone(text):
    """تحقق من صحة رقم الموبايل المصري"""
    import re
    digits = re.sub(r'\D', '', text)
    if len(digits) == 11 and digits.startswith("01"):
        return digits
    if len(digits) == 12 and digits.startswith("201"):
        return "0" + digits[2:]
    return None

