def _extract_budget(text):
    """استخلص رقم من نص الميزانية"""
    import re
    text = text.replace(",", "").replace("،", "")
    nums = re.findall(r'[\d]+', text)
    if not nums:
        return 0
    num = int(nums[0])
    if "مليون" in text or "ملون" in text:
        num *= 1_000_000
    elif "الف" in text or "ألف" in text or "k" in text.lower():
        num *= 1_000
    return num


