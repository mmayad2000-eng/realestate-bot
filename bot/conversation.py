# ===== كلمات مفتاحية لاكتشاف العملاء =====
TRIGGER_KEYWORDS = [
    "عاوز شقة", "عايز شقة", "محتاج شقة",
    "شقة للإيجار", "شقة للايجار", "شقة للبيع",
    "شقة في طنطا", "شقق طنطا", "شقق في طنطا",
    "فين أجيب شقة", "عاوز اشتري شقة", "عاوز اأجر شقة",
    "إيجار شقة", "بدور على شقة", "باحث عن شقة",
    "شقة مفروشة طنطا", "وحدة سكنية طنطا",
]

NOT_NOW_KEYWORDS = [
    "مش دلوقتي", "مش الوقت", "تاني يوم", "لحقاً", "بعدين",
    "مش جاهز", "لا شكراً", "لأ شكرا", "مش محتاج",
]

POSITIVE_KEYWORDS = [
    "أيوه", "ايوه", "اه", "نعم", "ماشي", "تمام", "اتفضل",
    "اتفضلي", "هيه", "طيب", "حاضر", "ممكن", "عندكم إيه",
    "عندكم ايه", "كمل", "ابدأ", "هيا",
]

def detect_intent(text):
    """اكتشف نية المستخدم من النص"""
    text_lower = text.lower().strip()
    
    for kw in TRIGGER_KEYWORDS:
        if kw in text_lower:
            return "trigger"
    
    for kw in NOT_NOW_KEYWORDS:
        if kw in text_lower:
            return "not_now"
    
    for kw in POSITIVE_KEYWORDS:
        if kw in text_lower:
            return "positive"
    
    # أرقام للاختيار
    if text_lower in ["1", "٢", "2", "٣", "3", "١"]:
        return "choice_" + text_lower
    
    return "unknown"

def parse_type_answer(text):
    """تحليل إجابة نوع الشقة"""
    text_lower = text.lower()
    if any(w in text_lower for w in ["1", "١", "إيجار سنوي", "ايجار سنوي", "سنوي"]):
        return "إيجار سنوي"
    elif any(w in text_lower for w in ["2", "٢", "شراء", "تمليك", "بيع"]):
        return "شراء (تمليك)"
    elif any(w in text_lower for w in ["3", "٣", "مفروش", "مفروشة"]):
        return "إيجار مفروش"
    return text  # الإجابة كما هي
